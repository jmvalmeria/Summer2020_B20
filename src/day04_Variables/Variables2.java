package day04_Variables;

public class Variables2 {
    public static void main(String[] args) {
        char ch1 = 'a';
        char ch2 = 3216; //This is the way to print a character
        char ch3 = 'b';
        char ch4 = 34571;

        System.out.println(ch1);
        System.out.println(ch2);

        System.out.println(ch1 + ch3); //Da como resultado el número y no la concatenación

        System.out.println(ch4);
    }
}
